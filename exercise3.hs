seconds :: (Int, Int) -> (Int, Int, Int) -> Int

seconds (d, mm) (h, m, s)
  | m==2 && d>28
    =(-1)
  | d>31 || mm>12 || h >= 24 || m > 59 || s > 59
    = (-1)
  | d<=0 || mm<=0 || h<0 || m<0 || s<0
    = (-1)
  | (mm<=6 && (mm `mod` 2 ==0) && d == 31)
    = (-1)
  | (mm>6 && (mm `mod` 2 ==1) && d == 31)
    = (-1)
  | otherwise
    = (dmm (d, mm)) + (hms (h, m, s))


dmm :: (Int, Int)-> Int
dmm (d, mm)
  | (d==1  && mm==1)
    = 0
  | mm==2
    = (d-1)*8600 + 31*86400
  | mm==1 && d<=31 && d>1
    = (d-1)*86400
  | (mm<=6 && (mm `mod` 2 ==0))
    = (((mm `div` 2)-1)*30*86400 + 28*86400 + ((mm `div` 2) -1)*31*86400 + d * 86400)
  |(mm >6 && (mm `mod` 2 == 1))
    = (((mm `div` 2)-1)*30*86400 + 28*86400 + (mm `div` 2)*31*86400 + d * 86400)
  | (mm<=6 && (mm `mod` 2 == 1) || (mm >6 && (mm `mod` 2 == 0)))
    = ((mm `div` 2)*31*86400 + (((mm - (mm `div` 2))-2)*30*86400) + 28*86400 + (d-1) * 86400)


hms :: (Int, Int, Int) -> Int
hms (h, m, s)
  | h>=0 && h<=23 && m>=0 && m<=59 && s>=0 && s<=59
    = (h*3600 + m*60 + s)
